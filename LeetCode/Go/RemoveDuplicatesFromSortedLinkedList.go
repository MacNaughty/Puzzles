func deleteDuplicates(head *ListNode) *ListNode {
	if head == nil {
		return nil
	}

	first := head

	currentNode := head

	for currentNode.Next != nil {
		if currentNode.Next.Val > currentNode.Val {
			currentNode = currentNode.Next
		} else {
			currentNode.Next = currentNode.Next.Next
		}
	}

	return first
}
